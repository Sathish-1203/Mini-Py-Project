# Largest of 3 numbers (int)- data type

a = int(input("A:"))
b = int(input("B:"))
c = int(input("C:"))
if a > b and a > c :
    print(a, "is largest")
elif b > c :
    print(b, "is largest")
else:
    print(c, "is largest")


# Largest of 3 numbers (float)- data type


a = float(input("A:"))
b = float(input("B:"))
c = float(input("C:"))
if a > b and a > c:
    print(a,"A is largest number")
elif b > c :
    print(b,"B is largest number")
else:
    print(c,"C is largest number")


